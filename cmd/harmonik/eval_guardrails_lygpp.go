package main

// eval_guardrails_lygpp.go — WS3e quality guardrails (hk-eval-prog-quality-guardrails-lygpp)
//
// Extends the WS3b metrics feeders (eval_metrics_cmd.go) with the guardrails
// from specs/02-quality-assessment.md §Part 3:
//   G2 — model-blind diff scrub: flag self-identifying strings in the diff.
//   G5 — metric gaming: flag a diff that touches the shipped test file.
//   G6 — rubric_version stamp on metrics.json (the collector, WS3c, stamps its
//        own copy onto eval-results.jsonl separately).
//   O-Q4 — second-judge cross-check: deterministic sample selection (sample-only
//        in v1). Invoking a second judge model is left to a follow-up wiring
//        change; this only decides which runs are in the sample.
//
// G1 (verbosity/line-budget) and G3 (hidden_test.go) feeders already exist in
// eval_metrics_cmd.go from WS3b; this file does not duplicate them.

import (
	"hash/fnv"
	"strings"
)

// evalRubricVersion is the rubric weight-set label stamped on metrics.json
// (G6). Bump only when the Q1-Q5 weights or definitions change; a report may
// only compare runs sharing the same rubric_version.
const evalRubricVersion = 1

// evalCrossCheckSampleRate is the fraction of runs selected for the optional
// second-judge cross-check (O-Q4). Sample-only in v1 per the design doc's
// "Lean: sample (cost)" note.
const evalCrossCheckSampleRate = 0.1

// evalSelfIDPatterns are case-insensitive substrings that indicate a diff
// line names the model/provider that produced it (G2). Matching lines are
// flagged, not stripped, so the evidence remains inspectable.
var evalSelfIDPatterns = []string{
	"generated by",
	"written by claude",
	"as an ai language model",
	"anthropic",
	"claude-opus",
	"claude-sonnet",
	"claude-haiku",
	"openai",
	"gpt-4",
	"gpt-3",
	"chatgpt",
	"gemini",
	"copilot",
}

// evalScrubSelfID scans added diff lines for self-identifying strings and
// returns the matching lines (trimmed), or an empty (non-nil) slice if none
// are found.
func evalScrubSelfID(diff string) []string {
	matches := []string{}
	for _, line := range strings.Split(diff, "\n") {
		if !strings.HasPrefix(line, "+") || strings.HasPrefix(line, "+++") {
			continue
		}
		lower := strings.ToLower(line)
		for _, pat := range evalSelfIDPatterns {
			if strings.Contains(lower, pat) {
				matches = append(matches, strings.TrimSpace(strings.TrimPrefix(line, "+")))
				break
			}
		}
	}
	return matches
}

// evalDiffTouchesTestFile reports whether the diff contains any hunk against
// a _test.go file (G5) — the implementer edited/deleted the shipped test
// rather than the solution, which would otherwise let a broken solution
// fool the deterministic grade or the Q2 feeder.
func evalDiffTouchesTestFile(diff string) bool {
	for _, line := range strings.Split(diff, "\n") {
		if strings.HasPrefix(line, "diff --git") && strings.Contains(line, "_test.go") {
			return true
		}
	}
	return false
}

// evalShouldCrossCheckSample deterministically decides whether a run is
// selected for the O-Q4 second-judge cross-check, based on a stable hash of
// the bead ID so repeated calls (and re-runs of the same bead) agree.
func evalShouldCrossCheckSample(beadID string) bool {
	h := fnv.New32a()
	_, _ = h.Write([]byte(beadID))
	bucket := float64(h.Sum32()%1000) / 1000.0
	return bucket < evalCrossCheckSampleRate
}
