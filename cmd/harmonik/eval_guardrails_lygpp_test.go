func TestEvalScrubSelfID_None(t *testing.T) {
	diff := `diff --git a/foo.go b/foo.go
+++ b/foo.go
+func Foo() string { return "bar" }
`
	got := evalScrubSelfID(diff)
	if len(got) != 0 {
		t.Errorf("got %v, want no matches", got)
	}
}

