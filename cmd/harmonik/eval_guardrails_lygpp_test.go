func TestEvalDiffTouchesTestFile_True(t *testing.T) {
	diff := `diff --git a/lru.go b/lru.go
+++ b/lru.go
+func Foo() {}
diff --git a/lru_test.go b/lru_test.go
+++ b/lru_test.go
+func TestFoo(t *testing.T) {}
`
	if !evalDiffTouchesTestFile(diff) {
		t.Error("want true when diff touches a _test.go file")
	}
}

func TestEvalDiffTouchesTestFile_False(t *testing.T) {
	diff := `diff --git a/lru.go b/lru.go
+++ b/lru.go
+func Foo() {}
`
	if evalDiffTouchesTestFile(diff) {
		t.Error("want false when diff does not touch a _test.go file")
	}
}

func TestEvalDiffTouchesTestFile_AddedFileFalse(t *testing.T) {
	// A net-new test file (new file mode) is a legitimate addition, not the
	// disallowed edit/delete of a shipped test, so G5 must not flag it.
	diff := `diff --git a/lru.go b/lru.go
+++ b/lru.go
+func Foo() {}
diff --git a/lru_test.go b/lru_test.go
new file mode 100644
index 0000000..abc1234
--- /dev/null
+++ b/lru_test.go
+package foo
+func TestFoo(t *testing.T) {}
`
	if evalDiffTouchesTestFile(diff) {
		t.Error("want false when the only _test.go section is a net-new file")
	}
}

func TestEvalDiffTouchesTestFile_AddedPlusModified(t *testing.T) {
	// A net-new test file alongside an edit to a shipped test still flags:
	// the shipped-test edit is the disallowed touch.
	diff := `diff --git a/new_test.go b/new_test.go
new file mode 100644
--- /dev/null
+++ b/new_test.go
+package foo
diff --git a/shipped_test.go b/shipped_test.go
index abc..def 100644
--- a/shipped_test.go
+++ b/shipped_test.go
+// tampered
`
	if !evalDiffTouchesTestFile(diff) {
		t.Error("want true when a shipped _test.go is edited even if a new test is added")
	}
}

func TestEvalShouldCrossCheckSample_Deterministic(t *testing.T) {
	beadID := "hk-eval-fizzbuzz-avjjr"
	first := evalShouldCrossCheckSample(beadID)
	for i := 0; i < 10; i++ {
		if evalShouldCrossCheckSample(beadID) != first {
			t.Fatalf("evalShouldCrossCheckSample(%q) is not deterministic across calls", beadID)
		}
	}
}

func TestEvalShouldCrossCheckSample_RoughlyMatchesRate(t *testing.T) {
	selected := 0
	const n = 2000
	for i := 0; i < n; i++ {
		beadID := "hk-eval-synthetic-" + string(rune('a'+i%26)) + string(rune('a'+(i/26)%26)) + string(rune('a'+(i/676)%26))
		if evalShouldCrossCheckSample(beadID) {
			selected++
		}
	}
	rate := float64(selected) / float64(n)
	if rate < evalCrossCheckSampleRate*0.5 || rate > evalCrossCheckSampleRate*1.5 {
		t.Errorf("sample rate = %.3f, want roughly %.3f", rate, evalCrossCheckSampleRate)
	}
}

func TestEvalComputeMetrics_GuardrailFields(t *testing.T) {
	goSrc := "package evaltest\n\n// Generated by Claude Opus.\nfunc Add(a, b int) int { return a + b }\n"
	dir := metricsTestRepo(t, "hk-eval-test-guard1", "eval-test", goSrc)

	rec, err := evalComputeMetrics(dir)
	if err != nil {
		t.Fatalf("evalComputeMetrics: %v", err)
	}
	if rec.RubricVersion != evalRubricVersion {
		t.Errorf("rubric_version = %d, want %d", rec.RubricVersion, evalRubricVersion)
	}
	if len(rec.SelfIDMatches) != 1 {
		t.Errorf("self_id_matches = %v, want 1 match", rec.SelfIDMatches)
	}
	if rec.TestFileTouched {
		t.Error("test_file_touched = true, want false (no test file in this diff)")
	}
}

// TestEvalComputeMetrics_HiddenTestFixture hardens the G3 hidden_test.go path
// (already implemented by WS3b's evalRunHiddenTest) with a real fixture, since
// no evaltasks/*/hidden_test.go previously existed to exercise it.
func TestEvalComputeMetrics_HiddenTestFixture(t *testing.T) {
	goSrc := "package evaltest\n\nfunc Double(n int) int { return n * 2 }\n"
	dir := metricsTestRepo(t, "hk-eval-hidden-zzzzz", "eval-hidden", goSrc)

	hiddenSrc := `package evaltest

import "testing"

func TestHiddenDouble(t *testing.T) {
	if Double(3) != 6 {
		t.Fatal("Double(3) != 6")
	}
}
`
	pkgDir := filepath.Join(dir, "evaltasks", "eval-hidden")
	if err := os.WriteFile(filepath.Join(pkgDir, "hidden_test.go"), []byte(hiddenSrc), 0o644); err != nil {
		t.Fatal(err)
	}
	commitCmds := [][]string{
		{"git", "-C", dir, "add", "."},
		{"git", "-C", dir, "commit", "-m", "add hidden test"},
	}
	for _, c := range commitCmds {
		if out, err := exec.Command(c[0], c[1:]...).CombinedOutput(); err != nil {
			t.Fatalf("%v: %v\n%s", c, err, out)
		}
	}

	rec, err := evalComputeMetrics(dir)
	if err != nil {
		t.Fatalf("evalComputeMetrics: %v", err)
	}
	if rec.HiddenTestPass == nil || !*rec.HiddenTestPass {
		t.Fatalf("hidden_test_pass = %v, want true", rec.HiddenTestPass)
	}
	if rec.HiddenTestPassCount == nil {
		t.Fatal("hidden_test_pass_count = nil, want 1")
	}
	if *rec.HiddenTestPassCount != 1 {
		t.Fatalf("hidden_test_pass_count = %d, want 1", *rec.HiddenTestPassCount)
	}
}
