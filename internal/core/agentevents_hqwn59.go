// AgentOutputChunkPayload is the typed event payload for the agent_output_chunk
// event (event-model.md §8.3.3).
//
// Tags: mechanism
// Axes: llm-freedom=none; io-determinism=best-effort; replay-safety=safe; idempotency=idempotent
// Durability class: L (lossy-tail-ok — per-chunk statistical aggregate; the
// improvement-loop subsystem requires per-chunk cost attribution but tolerated
// loss per §8.9 granularity note and EV-017/EV-INV-002).
//
// Emitted by the handler subprocess via the daemon watcher for each output
// chunk. chunk_index is monotonically increasing within a session and allows
// consumers to detect drops.
//
// # Payload fields (event-model.md §8.3.3 §6.3)
//
//   - run_id       — the run in whose context the chunk was emitted
//   - session_id   — handler-assigned session identifier
//   - chunk_index  — monotonic zero-based chunk counter within this session
//   - bytes_emitted — number of bytes in this chunk
//   - chunk_digest — optional content-addressable digest of this chunk (SHA-256 hex)
type AgentOutputChunkPayload struct {
	// RunID is the run in whose context the chunk was emitted.
	// Required (must not be uuid.Nil).
	RunID RunID `json:"run_id"`

	// SessionID is the handler-assigned session identifier. Required (non-empty).
	// UUIDv7 per handler-contract.md §4.1; opaque to non-handler consumers.
	SessionID SessionID `json:"session_id"`

	// ChunkIndex is the zero-based monotonic chunk counter within this session.
	// Required (must be >= 0).
	ChunkIndex int `json:"chunk_index"`

	// BytesEmitted is the number of bytes in this chunk. Required (must be >= 0).
	BytesEmitted int `json:"bytes_emitted"`

	// ChunkDigest is an optional SHA-256 hex digest of the chunk content.
	// Corresponds to chunk_digest? in §8.3.3 §6.3. Nil when not provided.
	// Non-nil must be non-empty.
	ChunkDigest *string `json:"chunk_digest,omitempty"`
}

// Valid reports whether p is a well-formed AgentOutputChunkPayload.
//
// Rules per event-model.md §8.3.3 §6.3:
//   - RunID must not be uuid.Nil.
//   - SessionID must be non-empty.
//   - ChunkIndex must be >= 0.
//   - BytesEmitted must be >= 0.
//   - ChunkDigest, when non-nil, must be non-empty.
func (p AgentOutputChunkPayload) Valid() bool {
	if uuid.UUID(p.RunID) == uuid.Nil {
		return false
	}
	if p.SessionID == "" {
		return false
	}
	if p.ChunkIndex < 0 {
		return false
	}
	if p.BytesEmitted < 0 {
		return false
	}
	if p.ChunkDigest != nil && *p.ChunkDigest == "" {
		return false
	}
	return true
}

