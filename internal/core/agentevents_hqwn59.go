//   - BeadID, when non-nil, must dereference to a non-empty value.
func (p SessionLogLocationPayload) Valid() bool {
	if uuid.UUID(p.RunID) == uuid.Nil {
		return false
	}
	if p.SessionID == "" {
		return false
	}
	if p.NodeID == "" {
		return false
	}
	if !p.AgentType.Valid() {
		return false
	}
	if p.LogPath == "" {
		return false
	}
	if p.LogFormat == "" {
		return false
	}
	if p.BeadID != nil && *p.BeadID == "" {
		return false
	}
	return true
}

// HandlerCapabilitiesPayload is the typed event payload for the
// handler_capabilities event (event-model.md §8.3.9).
//
// Tags: mechanism
// Axes: llm-freedom=none; io-determinism=best-effort; replay-safety=safe; idempotency=idempotent
// Durability class: O (ordinary — version-negotiation observability; the
// orchestrator uses this event to confirm protocol version compatibility per
// handler-contract.md §4.1 §8.1.10).
//
// Emitted by the handler subprocess via the daemon watcher as the first
// message of the handler session, advertising supported protocol versions.
// The orchestrator uses this for version negotiation per handler-contract.md §8.1.10.
//
// # Payload fields (event-model.md §8.3.9)
//
//   - run_id                    — the run in whose context the handler reported capabilities
//   - session_id                — handler-assigned session identifier
//   - protocol_versions_supported — list of handler-contract protocol version strings this handler supports
type HandlerCapabilitiesPayload struct {
	// RunID is the run in whose context the handler reported capabilities.
	// Required (must not be uuid.Nil).
	RunID RunID `json:"run_id"`

	// SessionID is the handler-assigned session identifier. Required (non-empty).
	// UUIDv7 per handler-contract.md §4.1; opaque to non-handler consumers.
	SessionID SessionID `json:"session_id"`

	// ProtocolVersionsSupported is the list of protocol version strings supported
	// by this handler. Required (non-nil; must be non-empty to be meaningful
	// for version negotiation per handler-contract.md §8.1.10).
	ProtocolVersionsSupported []string `json:"protocol_versions_supported"`
}

// Valid reports whether p is a well-formed HandlerCapabilitiesPayload.
//
// Rules per event-model.md §8.3.9:
//   - RunID must not be uuid.Nil.
//   - SessionID must be non-empty.
//   - ProtocolVersionsSupported must be non-nil and non-empty.
func (p HandlerCapabilitiesPayload) Valid() bool {
	if uuid.UUID(p.RunID) == uuid.Nil {
		return false
	}
	if p.SessionID == "" {
		return false
	}
	if len(p.ProtocolVersionsSupported) == 0 {
		return false
	}
	return true
}
